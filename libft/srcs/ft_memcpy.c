#include <libft.h>

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;
	char	*str;
	char	*ptr;

	str = (char*)dest;
	ptr = (char*)src;
	i = -1;
	while (++i < n)
		str[i] = ptr[i];
	return (dest);
}
